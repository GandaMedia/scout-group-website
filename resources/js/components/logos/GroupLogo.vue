function fitText(
    text: string,
    baseFontSize: number,
    minimumFontSize: number,
    maxWidth: number,
): TextFit {
    const estimatedWidth = Math.max(text.trim().length, 1) * baseFontSize * 0.6;

    if (estimatedWidth <= maxWidth) {
        return { fontSize: baseFontSize };
    }

    const scaledFontSize = Math.max(
        minimumFontSize,
        baseFontSize * (maxWidth / estimatedWidth),
    );

    return {
        fontSize: Number(scaledFontSize.toFixed(2)),
        textLength: scaledFontSize === minimumFontSize ? maxWidth : undefined,
    };
}
